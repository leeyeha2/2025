import streamlit as st

# 웹 앱의 기본 설정 (페이지 타이틀 등)
st.set_page_config(
    page_title="하룰랄라의 퍼스널 컬러 화장품 추천",
    layout="centered",
    initial_sidebar_state="expanded"
)

